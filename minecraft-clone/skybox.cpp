shader_program skybox_program;
unsigned int skyboxVAO, skyboxVBO;
unsigned int skybox_texture;

void initialize_skybox_resources(unsigned int texture)
{
	skybox_program = create_program("D:/Workspace/Resources/skybox.vs", "D:/Workspace/Resources/skybox.fs");

    // skybox VAO
    glGenVertexArrays(1, &skyboxVAO);
    glGenBuffers(1, &skyboxVBO);
    glBindVertexArray(skyboxVAO);
    glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

    skybox_texture = texture;

	skybox_program.use();
	skybox_program.setInt("skybox", 0);
}

void draw_skybox(glm::mat4 model, glm::mat4 view, glm::mat4 projection, glm::vec3 position) 
{
    // draw skybox as last
    glDepthFunc(GL_LEQUAL);

    skybox_program.update_program(
        model,
        glm::mat4(glm::mat3(view)),
        projection,
        position);

    // skybox cube
    glBindVertexArray(skyboxVAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_CUBE_MAP, skybox_texture);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

    glDepthFunc(GL_LESS); // set depth function back to default
}

